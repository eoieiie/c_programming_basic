// C = structure 한 언어. 
// 분기 = 판단과 기준에 따라서 나뉘는 것. 

// 관계 연산자 = 이항의 관계를 비교한다. (a가 b보다 ~)
// 등가 연산자 = 두 항의 동일함을 판단한다. (a와 b는 같다)
// 논리 연산자 = not(!), and(&&), or(||)

// 연산의 결과는 false(zero) 아니면 true(nonzero) 둘 중 하나이다.



#include <stdio.h>

int main(void)
{
    char c = 'w';
    int a, b, c, d, e, q, p, f,  i = 1, j = 2,  k =-7;
    double x = 7e+33, y =  0.001;

    'a' + 1 < c;        // ('a' + 1) < c                1
    -i -5 * j <= k + 1; //((-i) - (5*j)) >= (k + 1)     0  마이너스 기호(부호 바꾸기), 곱셈기호, 비교연산 시험에 이렇게 나옵니다~
    3 < j < 5;           // (3 < j) < 5                  1  3 - j로 판단. bool 값이 0 이고, 그 다음 0 - 5 로 비교.값은 마이너스이므로 1
    x < x + y;           // x < (x + y)                  0  7e+33 이건 매우 큰 숫자이고, 0.001은 상대적으로 매우 작음. 
    // 이렇게 큰 차이가 나는 두 수를 더할 때, 부동 소수점 연산의 한계로 인해 작은 숫자가 소멸되어 버릴 수 있음
    
    //뤼튼 가라사대, 

    //이 표현식은(3 < j < 5) 일반적인 수학적 표현과 다르게 동작합니다. 
    //C에서는 (3<j)가 먼저 계산되고, 그 결과(0 또는 1)가 다음 연산인 <5와 비교됩니다. 
    //따라서 j가 어떤 값을 가지더라도 이 표현식의 결과는 항상 참(즉, 값은 항상 "1")입니다.


    //연산자 우선순위와 결합법칙에 관한 추가 예제:

    a = b = c = d = e;
    // 대입 연산자는 오른쪽에서 왼쪽으로 결합되므로 e의 값을 d, c, b, a 모두에 대입합니다.

    a * b / c;
    // , / 연산자들이 같은 우선 순위를 가지므로 왼쪽에서 오른쪽으로 결합됩니다: ((ab)/c)
    
    a || b && c || d;
    // 논리 AND (&&) 연산자가 OR (||) 보다 우선 순위가 높으므로 이 식은 a || (b && c) || d 와 같습니다.
    
    !p && q;
    // 단항 연산자인 ! 가 && 보다 우선 순위가 높으므로 (!p) && q 와 같습니다.
    
    i++ * ++j;
    // 단항연산과 산술연산이 함께 있는 경우, 단항연산이 우선순위가 더 높습니다. 그러므로 (i++) * (++j)와 같습니다. 이 때 i++는 i를 사용한 후에 1을 증가시키고, ++j는 j를 1 증가시킨 후에 그 값을 사용합니다.
    
    a = b + c > d ? e : f;
    // 조건 연산자 ?: 의 우선 순위는 대입 연산자 = 보다 높으므로 a = ((b + c > d) ? e : f) 와 같습니다.
    

    i == j;                     //j == i                                0
    i != j;                     //j != i                                1
    i + j + k == -2 * -k;       //((i + j) + k) == ((-2) * (-k))        1


    return 0;
}

